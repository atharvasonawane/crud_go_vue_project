func resolveLogoPath() string {
	wd, _ := os.Getwd()
	candidates := []string{
		filepath.Join(wd, "assets", "logo.png"),
		filepath.Join(wd, "backend", "assets", "logo.png"),
	}

	if _, thisFile, _, ok := runtime.Caller(0); ok {
		handlersDir := filepath.Dir(thisFile)
		backendDir := filepath.Clean(filepath.Join(handlersDir, ".."))
		candidates = append(candidates, filepath.Join(backendDir, "assets", "logo.png"))
	}

	for _, p := range candidates {
		if _, err := os.Stat(p); err == nil {
			abs, err := filepath.Abs(p)
			if err == nil {
				return abs
			}
			return p
		}
	}
	return ""
}

